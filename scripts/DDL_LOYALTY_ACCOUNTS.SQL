CREATE TABLE KOHLS.LOYALTY_ACCOUNTS (
    ID INT PRIMARY KEY AUTO_INCREMENT,
	EVENT_ID BIGINT(20),
	LOYALTY_ACCOUNT_EMAIL VARCHAR(255),
	REWARD_ID BIGINT(20),
	REWARD_DOLLAR_AMOUNT VARCHAR(255),
	REWARD_VALUE INT(20),
	LOYALTY_ACCOUNT_ID BIGINT(20),
	LOYALTY_SERVICE_ID BIGINT(20),
	REWARD_DATE TIMESTAMP NULL DEFAULT NULL,
	OFFER_ID VARCHAR(255),
	OFFER_NAME VARCHAR(255),
	ISSUE_DATE TIMESTAMP NULL DEFAULT NULL,
	POINT_VALUE VARCHAR(255),
	EXPIRATION_DATE TIMESTAMP NULL DEFAULT NULL,
    STATUS ENUM ('NEW', 'PROCESSING', 'COMPLETED', 'FAILED')
);
